# Цикл с вопросом добавить ли еще заметку или удалить
while True:
    all_notes()
    answ = input("Хотите добавить ещё одну заметку или удалить? (да/нет/удалить): ")
    if answ == "да":
        new_note()
    elif answ == "нет":
        all_notes()
        break
    elif answ == "удалить": del_note()
    else: print("Введите ответ корректно!")







